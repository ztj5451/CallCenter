package com.callCenter.entity;

public class CustomerInfo {
	private String id;
	private String address;
	private String kh_tel;
	private String kh_name;
	private String kh_sex;
	private String kh_cardId;
	private String moneys;
	private String jg;
	
	public String getJg() {
		return jg;
	}

	public void setJg(String jg) {
		this.jg = jg;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getKh_tel() {
		return kh_tel;
	}

	public void setKh_tel(String kh_tel) {
		this.kh_tel = kh_tel;
	}

	public String getKh_name() {
		return kh_name;
	}

	public void setKh_name(String kh_name) {
		this.kh_name = kh_name;
	}

	public String getKh_sex() {
		return kh_sex;
	}

	public void setKh_sex(String kh_sex) {
		this.kh_sex = kh_sex;
	}

	public String getKh_cardId() {
		return kh_cardId;
	}

	public void setKh_cardId(String kh_cardId) {
		this.kh_cardId = kh_cardId;
	}

	public String getMoneys() {
		return moneys;
	}

	public void setMoneys(String moneys) {
		this.moneys = moneys;
	}

}
